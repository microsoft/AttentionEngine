@triton.testing.perf_report(configs)
def bench_flash_attention(BATCH, H, Q_CTX, KV_CTX, HEAD_DIM, causal, mode, provider, device="cuda"):
    assert mode in ["fwd", "bwd"]
    warmup = 25
    rep = 100
    dtype = torch.float16
    if "triton" in provider:
        q = torch.empty((BATCH, H, Q_CTX, HEAD_DIM), dtype=dtype, device=device).normal_(-1.0, 1.0)
        k = torch.empty((BATCH, H, KV_CTX, HEAD_DIM), dtype=dtype, device=device).normal_(-1.0, 1.0)
        v = torch.empty((BATCH, H, KV_CTX, HEAD_DIM), dtype=dtype, device=device).normal_(-1.0, 1.0)
        q.requires_grad = k.requires_grad = v.requires_grad = True
        if mode == "fwd" and "fp8" in provider:
            q = q.to(torch.float8_e5m2)
            k = k.to(torch.float8_e5m2)
            v = v.permute(0, 1, 3, 2).contiguous()
            v = v.permute(0, 1, 3, 2)
            v = v.to(torch.float8_e5m2)
        sm_scale = 1.3
        fn = lambda: attention(q, k, v, causal, sm_scale)
        if mode == "bwd":
            o = fn()
            do = torch.randn_like(o)
            fn = lambda: o.backward(do, retain_graph=True)
        ms = triton.testing.do_bench(fn, warmup=warmup, rep=rep)
        print(f"{provider} {mode} {ms} ms")
    flops_per_matmul = 2.0 * BATCH * H * Q_CTX * KV_CTX * HEAD_DIM
    total_flops = 2 * flops_per_matmul
    if causal:
        total_flops *= 0.5
    if mode == "bwd":
        total_flops *= 2.5  # 2.0(bwd) + 0.5(recompute)
    return total_flops / ms * 1e-9


if __name__ == "__main__":
    # only works on post-Ampere GPUs right now
    bench_flash_attention.run(save_path=".", print_data=True)
